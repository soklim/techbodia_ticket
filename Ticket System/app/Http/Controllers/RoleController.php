     * @return \Illuminate\Http\Response
     */
  

    public function Save(Request $request)
    {
        if ($request->id == 0){
            $input['name'] = $request->name;
            Role::create($input);
        }
        else{
            $input = $request->all();
            $role = Role::find($request->id);
            $role->update($input);
        }
        return Response()->json(array(
            'code' => 0,
        ));
    }
    public function  getData(){

        $data = DB::table('roles')
